public class Config {
	
	String api_token;
	String api_deck_id;
	String api_mission_id;
	String time;
	String ip;
	String debug;
	
	public String getApi_token() {
		return api_token;
	}
	public void setApi_token(String api_token) {
		this.api_token = api_token;
	}
	public String getApi_deck_id() {
		return api_deck_id;
	}
	public void setApi_deck_id(String api_deck_id) {
		this.api_deck_id = api_deck_id;
	}
	public String getApi_mission_id() {
		return api_mission_id;
	}
	public void setApi_mission_id(String api_mission_id) {
		this.api_mission_id = api_mission_id;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getIp() {
		return ip;
	}
	public void setIp(String ip) {
		this.ip = ip;
	}
	public String getDebug() {
		return debug;
	}
	public void setDebug(String debug) {
		this.debug = debug;
	}
	
}
